n1 = float(input('Digite a n1: '))
n2 = float(input('Digite a n2: '))
n3 = float(input('Digite a n3: '))
n4 = float(input('Digite a n4: '))


media = (n1+n2+n3+n4)/4


if media == 6:
    print('Sua média foi: ', media ,' você está na média')

elif media < 6:
    print('Sua média foi: ', media ,' você está abaixo média')

elif media > 6:
    print('Sua média foi: ', media ,' você está acima da média')



